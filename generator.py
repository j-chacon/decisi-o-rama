# DEfine the generator of the tuncated normal distributions
def tn(mu, sigma, lower=-np.inf, upper=np.inf):
    out = truncnorm((lower - mu) / sigma, 
                    (upper - mu) / sigma, 
                    loc=mu, 
                    scale=sigma)
    return out.rvs()

